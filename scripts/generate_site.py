def update_root_readme() -> None:
    readme = ROOT / 'README.md'
    original = readme.read_text().rstrip() + '\n\n'
    note = '## Website\n\nA simple GitHub Pages-ready website is available in [`docs/`](./docs/) with a separate page for every recipe card and beginner-friendly cooking guidance. Open [`docs/index.html`](./docs/index.html) locally or publish the `docs/` folder with GitHub Pages.\n'
    if '## Website' not in original:
        readme.write_text(original + note)


def main() -> None:
    recipes = [parse_recipe(p) for p in sorted(RECIPES_DIR.glob('day-*.md'))]
    DOCS_DIR.mkdir(exist_ok=True)
    (DOCS_DIR / 'styles.css').write_text(STYLE)
    (DOCS_DIR / 'index.html').write_text(render_index(recipes))
    for recipe in recipes:
        (DOCS_DIR / recipe['filename']).write_text(render_page(recipe, recipes))
    update_root_readme()
    print(f'Generated {len(recipes)} recipe pages plus index into {DOCS_DIR}')

if __name__ == '__main__':
    main()
