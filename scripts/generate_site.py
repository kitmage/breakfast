    if has_word(recipe, 'smoothie', 'pudding', 'overnight'):
        tips.append('Too thick: add a small splash of milk or water and stir or blend again.')
    if has_word(recipe, 'beans', 'chickpeas', 'lentils'):
        tips.append('Legumes still tough: they simply need more cooking time. Simmer longer and check every 10 minutes.')
    if has_word(recipe, 'egg', 'eggs'):
        tips.append('Eggs browning too fast: lower the heat. Eggs cook better gently than quickly.')
    tips.append('Not flavorful enough: add a pinch of salt, a squeeze of lemon, or a dusting of your favorite spice blend.')
    return tips


