def bean_section(recipe: dict) -> tuple[str, list[str]] | None:
    combined = recipe['name'].lower() + ' ' + ' '.join(i['item'].lower() for i in recipe['ingredients'])
    if 'lentil' in combined:
        return ('How to prepare lentils from dry pantry stock', BEAN_GUIDE['lentils'])
    if 'chickpea' in combined:
        return ('How to prepare chickpeas from dry pantry stock', BEAN_GUIDE['chickpeas'])
    if 'black bean' in combined or 'white bean' in combined or 'bean' in combined or 'beans' in combined:
        return ('How to prepare beans from dry pantry stock', BEAN_GUIDE['beans'])
    return None


