def slugify(text: str) -> str:
    text = text.lower().strip()
    text = re.sub(r'[^a-z0-9]+', '-', text)
    return text.strip('-')


def parse_recipe(path: Path) -> dict:
    text = path.read_text()
    _, front, body = text.split('---', 2)
    day = int(re.search(r'day: (\d+)', front).group(1))
    name = re.search(r'name: "([^"]+)"', front).group(1)
    servings = re.search(r'servings: (\d+)', front)
    servings = int(servings.group(1)) if servings else 1
    ingredients = []
    current = None
    for line in front.splitlines():
        item_match = re.match(r'\s*- item: "([^"]+)"', line)
        if item_match:
            if current:
                ingredients.append(current)
            current = {'item': item_match.group(1)}
            continue
        if current is not None:
            q = re.match(r'\s+quantity: "([^"]+)"', line)
            u = re.match(r'\s+unit: "([^"]+)"', line)
            if q:
                current['quantity'] = q.group(1)
            elif u:
                current['unit'] = u.group(1)
    if current:
        ingredients.append(current)
    short = []
    for line in body.splitlines():
        m = re.match(r'\d+\.\s+(.*)', line.strip())
        if m:
            short.append(m.group(1))
    tags = re.findall(r'-\s+([a-z-]+)', body.split('## Shopping Tags')[-1]) if '## Shopping Tags' in body else []
    filename = f"recipe-{day:02d}-{slugify(name)}.html"
    return {
        'day': day,
        'name': name,
        'servings': servings,
        'ingredients': ingredients,
        'short_instructions': short,
        'tags': tags,
        'filename': filename,
        'path': path,
    }


def fmt_ingredient(ing: dict) -> str:
    q = ing.get('quantity', '').strip()
    u = ing.get('unit', '').strip()
    item = ing['item']
    if q and u:
        return f"{q} {u} {item}"
    if q:
        return f"{q} {item}"
    return item


def detect_method(recipe: dict) -> tuple[str, list[str]]:
    text = recipe['name'].lower()
    for key, value in METHOD_OVERRIDES.items():
        if key in text:
            return value
    return 'assemble', ['bowl', 'spoon', 'plate']


def has_word(recipe: dict, *words: str) -> bool:
    combined = recipe['name'].lower() + ' ' + ' '.join(i['item'].lower() for i in recipe['ingredients'])
    return any(word in combined for word in words)


